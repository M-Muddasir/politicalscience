import { prisma } from '@/lib/prisma';
import { NextRequest, NextResponse } from 'next/server';

// GET a single course by ID
export async function GET(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> } // ID comes as a string
) {
  try {
    const { id } = await params;
    const course = await prisma.course.findUnique({
      where: {
        id: id
      },
      include: {
        program: {
          select: {
            name: true,
            degreeType: true
          }
        }
      }
    });
    
    if (!course) {
      return NextResponse.json({ error: 'Course not found' }, { status: 404 });
    }
    
    return NextResponse.json(course);
  } catch (error) {
    console.error('Error fetching course:', error);
    return NextResponse.json({ error: 'Failed to fetch course' }, { status: 500 });
  }
}

// PATCH to update a course
export async function PATCH(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  try {
    const data = await request.json();
    
    // Ensure programId is an integer if provided
    if (data.programId && typeof data.programId === 'string') {
      data.programId = parseInt(data.programId);
    }
    
    const course = await prisma.course.update({
      where: {
        id: id
      },
      data
    });
    
    return NextResponse.json(course);
  } catch (error) {
    console.error('Error updating course:', error);
    return NextResponse.json({ error: 'Failed to update course' }, { status: 500 });
  }
}

// DELETE a course
export async function DELETE(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  try {
    await prisma.course.delete({
      where: {
        id: id
      }
    });
    
    return NextResponse.json({ message: 'Course deleted successfully' });
  } catch (error) {
    console.error('Error deleting course:', error);
    return NextResponse.json({ error: 'Failed to delete course' }, { status: 500 });
  }
}
